/* Imagine que queremos calcular a área de um retângulo, precisamos só de duas variáveis para calcular
    sendo elas largura e altura, basta declararmos-as e imprimir o resultado, mas imagine que precisemos
    calcular a área de 10 retângulos, fica inviàvel declarar 20 variáveis! Para isso existem as funções,
    elas nos permitem a reutilização de código
*/
function areaRet(largura, altura){
    return largura * altura;
}